// What is Unit Testing?

// Definition: Testing individual units (functions, classes, methods) of your code in isolation to make sure they work as expected.

// Goal: Catch bugs early and ensure code behaves correctly when changes are made.

// Popular JS Testing Frameworks

// Jest – Very popular, comes with assertion library, mocks, and coverage.

// Mocha + Chai – Flexible, Mocha for running tests, Chai for assertions.

// Vitest – Modern and fast, good for frontend frameworks like Vue/React.
